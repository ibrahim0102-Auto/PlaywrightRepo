
function NumType(number) {
    if (number > 0) {
        console.log("Given Number is a positive number->" + number)
    }
    else if (number < 0) {
        console.log("Given Number is a negative number->" + number)
    }
    else {
        console.log("Given Number is Zero->" + number)
    }
}

let num = -10;
NumType(num);

